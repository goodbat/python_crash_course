# Use whitespace to format your output
print("Python")

# Add a tab in front of a string
print("\tPython")

# Break up a string with newline characters
print("Languages:\nPython\nC\nJavaScript")

# Combine the two strategies
print("Languages:\n\tPython\n\tC\n\tJavaScript")

# Stripping whitespace
favorite_language = ' python '
print("String is", favorite_language, "!")
print("String is", favorite_language.rstrip(), "!")
print("String is", favorite_language.lstrip(), "!")
print("String is", favorite_language.strip(), "!")